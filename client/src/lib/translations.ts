export function getTranslation(key: string, language: string): string {
  const translations = TRANSLATIONS[language] || TRANSLATIONS.en_IN;
  return translations[key] || key;
}

export function formatTranslation(key: string, language: string, values: Record<string, string>): string {
  let text = getTranslation(key, language);
  Object.entries(values).forEach(([k, v]) => {
    text = text.replace(`{${k}}`, v);
  });
  return text;
}
